            <h2>mission</h2>
            <p>We believe children should stay children as long as they need and the fact that they enter 1st grade should not end their childhood. We intertwine playing and learning at such an early age and encourage our students to enjoy learning.</p>
        </section>
        <section class="about">
            <h2>about us</h2>
            <p>We began as a visionary venture in 1998 when Dr. Gabriel and Alish Black, world-class academics in the field of economics, endeavored to find a way to prepare students for college and the real world better than schools did at the time. They quickly found that the best way to make students thrive was simple: challenge them. After opening their first school in 1998, the Blocks discovered that students, when given a well-crafted curriculum and teachers who truly cared about teaching it, were able to learn at exceedingly high levels, meeting and surpassing the best students in the world. What began as a single campus has grown to become a unique, high-achieving, and unprecedented network of schools.</p>
        </section>
